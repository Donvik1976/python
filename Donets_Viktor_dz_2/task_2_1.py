# Выяснить тип результата выражений:
# ● 15 * 3
# ● 15 / 3
# ● 15 // 2
# ● 15 ** 2


print('Тип результата выраженя 15 * 2 - это ', type(15 * 2))
print('Тип результата выраженя 15 / 3 - это ', type(15 / 3))
print('Тип результата выраженя 15 // 2 - это ', type(15 // 2))
print('Тип результата выраженя 15 ** 2 - это ', type(15 ** 2))
